// Function to read all talents from the JSON file
function readTalents($filePath) {
    if (!file_exists($filePath) || filesize($filePath) == 0) {
        return []; // Return empty array if file doesn't exist or is empty
    }
    $jsonContent = file_get_contents($filePath);
    $talents = json_decode($jsonContent, true); // Decode as associative array
    return is_array($talents) ? $talents : []; // Ensure it's an array
}

// Read all talents
$allTalents = readTalents($jsonFilePath);

// Sort talents by timestamp in descending order (newest first)
// The 'timestamp' key generated by stdtalentpost.php is in 'Y-m-d H:i:s' format,
// so we need to convert them to Unix timestamps for proper comparison.
usort($allTalents, function($a, $b) {
    $timeA = strtotime($a['timestamp'] ?? '1970-01-01 00:00:00'); // Fallback for missing timestamp
    $timeB = strtotime($b['timestamp'] ?? '1970-01-01 00:00:00'); // Fallback for missing timestamp
    return $timeB <=> $timeA; // $b first for descending order
});


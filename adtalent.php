            <?php
            // Check if the talent data directory exists and is readable
            if (!is_dir($talentDataDir) || !is_readable($talentDataDir)) {
                echo '<p class="no-posts-message"><strong>Error:</strong> The talent data directory ("' . htmlspecialchars($talentDataDir) . '") does not exist or is not readable. Please ensure it\'s created and has proper permissions.</p>';
            } elseif (!file_exists($jsonFilePath) || filesize($jsonFilePath) == 0) {
                echo '<p class="no-posts-message">No talent posts yet. Be the first to share your amazing work!</p>';
            } elseif (!is_dir($upload_dir) || !is_readable($upload_dir)) {
                echo '<p class="no-posts-message"><strong>Error:</strong> The uploads directory ("' . htmlspecialchars($upload_dir) . '") does not exist or is not readable. Please ensure it\'s created and has proper permissions.</p>';
            } else {
                // If there are no talents after reading the file (e.g., empty array from JSON)
                if (empty($allTalents)) {
                    echo '<p class="no-posts-message">No talent posts yet. Be the first to share your amazing work!</p>';
                } else {
                    // Display each post in a card format
                    foreach ($allTalents as $post) {
                        // Ensure all expected keys exist to prevent warnings
                        $id = htmlspecialchars($post['id'] ?? uniqid('unknown_')); // Fallback ID
                        $rollNumber = htmlspecialchars($post['roll_number'] ?? 'N/A');
                        $name = htmlspecialchars($post['name'] ?? 'Unknown Talent');
                        $description = nl2br(htmlspecialchars($post['description'] ?? 'No description provided.'));
                        $mediaPath = htmlspecialchars($post['media_path'] ?? ''); // This is the full path including directory
                        $externalLink = htmlspecialchars($post['external_link'] ?? '');
                        $timestamp = htmlspecialchars($post['timestamp'] ?? date('Y-m-d H:i:s')); // Fallback timestamp
                        
                        echo '<div class="post-card">';
                        echo '<h3>' . $name . '</h3>';
                        echo '<p>' . $description . '</p>';
                        
                        // Handle uploaded media (image, video, audio)
                        if (!empty($mediaPath)) {
                            // Check if the file actually exists
                            if (file_exists($mediaPath)) {
                                $fileExtension = strtolower(pathinfo($mediaPath, PATHINFO_EXTENSION));

                                echo '<div class="uploaded-content">';
                                if (in_array($fileExtension, ['jpg', 'jpeg', 'png', 'gif', 'webp'])) {
                                    echo '<img src="' . $mediaPath . '" alt="Uploaded Image for ' . $name . '">';
                                } elseif (in_array($fileExtension, ['mp4', 'webm', 'ogg'])) {
                                    echo '<video controls src="' . $mediaPath . '" alt="Uploaded Video for ' . $name . '"></video>';
                                } elseif (in_array($fileExtension, ['mp3', 'wav', 'ogg'])) {
                                    echo '<audio controls src="' . $mediaPath . '"></audio>';
                                } else {
                                    // Fallback for unexpected file types
                                    echo '<p>Download: <a href="' . $mediaPath . '" download="' . basename($mediaPath) . '"><i class="fas fa-download"></i> ' . basename($mediaPath) . '</a></p>';
                                }
                                echo '</div>'; // End uploaded-content
                            } else {
                                // Inform if the linked file is missing
                                echo '<div class="uploaded-content"><p style="color: red;"><em>Uploaded media not found: ' . basename($mediaPath) . '</em></p></div>';
                            }
                        }

                        // Display external link if available
                        if (!empty($externalLink)) {
                            // Basic URL validation
                            if (filter_var($externalLink, FILTER_VALIDATE_URL)) {
                                echo '<p class="external-link">External Link: <a href="' . $externalLink . '" target="_blank" rel="noopener noreferrer"><i class="fas fa-external-link-alt"></i> ' . $externalLink . '</a></p>';
                            } else {
                                echo '<p class="external-link" style="color: red;"><em>Invalid external link: ' . $externalLink . '</em></p>';
                            }
                        }
                        
                        // Display post metadata (author and date)
